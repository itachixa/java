
public class EXO12 {
    public static void main(String[] args) {
        for (int i = 6; i <= 9; i++) {
            System.out.println("Table de " + i + " :");
            for (int j = 1; j <= 12; j++) {
                System.out.println(i + " x " + j + " = " + (i * j));
            }
            System.out.println();
        }
    }
}
